package decorator.lambda.decorator;

public class NumberDecorators {
    public static Integer addDouble(Integer input) {
        return input+input;
    }
    public static Integer addOne(Integer input) {
        return input+1;
    }
    public static Integer addTwo(Integer input) {
        return input+2;
    }
    public static Integer addThree(Integer input) {
        return input+3;
    }

}
